# <Data types>

# Integer type
x = 5
age = 23
count_app = 34
minus = -30

print(x, age, count_app, minus)

# Float type
pi = 3.141592
num1 = 12.5
num2 = -4.28

print(pi, num1, num2)

# String type
name = 'minsoo'
language = 'Python'
Language = "C++"

print(name, language, Language)

# Boolean type
flag = True
running = False

print(flag, running)